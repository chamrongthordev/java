package com.company;

import static java.lang.System.out;

public class Main {

  public static void main(String[] args) {
    byte byteNumber = 1;
    short shortNumber = 1;
    int intNumber = 1;
    float floatNumber = 1.0F;
    double doubleNumber = 2.0;

    String stringVariable = "hello, world!";

    out.println("byteNumber: " + byteNumber);
    out.println("shortNumber: " + shortNumber);
    out.println("intNumber: " + intNumber);
    out.println("floatNumber: " + floatNumber);
    out.println("doubleNumber: " + doubleNumber);
    out.println("stringNumber: " + stringVariable);
  }
}
